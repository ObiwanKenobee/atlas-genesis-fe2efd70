#!/usr/bin/env python3
import os
import shutil
import stat


def remove_readonly(func, path, exc_info):
    excvalue = exc_info[1]
    if not isinstance(excvalue, PermissionError):
        raise
    os.chmod(path, stat.S_IRWXU)
    func(path)


def delete_node_modules():
    root_dir = os.getcwd()
    node_modules_path = os.path.join(root_dir, "node_modules")
    package_lock_path = os.path.join(root_dir, "package-lock.json")

    print("Deleting node_modules directory...")
    if os.path.exists(node_modules_path):
        try:
            shutil.rmtree(node_modules_path, onerror=remove_readonly)
            print("node_modules directory deleted successfully")
        except Exception as exc:
            print(f"Error deleting node_modules directory: {exc}")
    else:
        print("node_modules directory not found")

    print("Deleting package-lock.json...")
    if os.path.exists(package_lock_path):
        try:
            os.remove(package_lock_path)
            print("package-lock.json deleted successfully")
        except Exception as exc:
            print(f"Error deleting package-lock.json: {exc}")
    else:
        print("package-lock.json not found")

    print("Cleanup complete")


if __name__ == "__main__":
    delete_node_modules()
