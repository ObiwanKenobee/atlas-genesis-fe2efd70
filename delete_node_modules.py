#!/usr/bin/env python3
import os
import sys

def delete_node_modules():
    node_modules_path = os.path.join(os.getcwd(), "node_modules")
    package_lock_path = os.path.join(os.getcwd(), "package-lock.json")
    
    print("Deleting node_modules directory...")
    if os.path.exists(node_modules_path):
        try:
            for root, dirs, files in os.walk(node_modules_path, topdown=False):
                for file in files:
                    file_path = os.path.join(root, file)
                    try:
                        os.remove(file_path)
                    except Exception as e:
                        print(f"Error deleting file {file_path}: {e}")
                for dir in dirs:
                    dir_path = os.path.join(root, dir)
                    try:
                        os.rmdir(dir_path)
                    except Exception as e:
                        print(f"Error deleting directory {dir_path}: {e}")
            os.rmdir(node_modules_path)
            print("node_modules directory deleted successfully")
        except Exception as e:
            print(f"Error deleting node_modules directory: {e}")
    
    print("Deleting package-lock.json...")
    if os.path.exists(package_lock_path):
        try:
            os.remove(package_lock_path)
            print("package-lock.json deleted successfully")
        except Exception as e:
            print(f"Error deleting package-lock.json: {e}")
    
    print("Cleanup complete")

if __name__ == "__main__":
    delete_node_modules()
